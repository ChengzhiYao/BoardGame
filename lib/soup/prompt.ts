// 海龟汤（情境推理）模式的 prompt 构建。

export interface SoupGenOptions {
  difficulty?: string;   // 普通 / 困难 / 地狱
  supernatural?: string; // any 不限 / allow 灵异 / real 纯现实
  tone?: string;         // any 不限 / 悬疑 / 惊悚 / 温情 / 黑色幽默 / 搞笑
  gore?: number;         // 0-100 血腥程度
  horror?: number;       // 0-100 恐怖程度
  edge?: number;         // 0-100 尺度（暗黑 / 成人向）
}

function bucket(v: number, arr: string[]): string {
  return arr[Math.min(arr.length - 1, Math.max(0, Math.round((v / 100) * (arr.length - 1))))];
}

// 生成一道海龟汤：汤面（玩家可见的诡异情境）+ 汤底（隐藏真相）。
export function buildSoupGenPrompt(opts?: SoupGenOptions | string) {
  // 兼容旧调用：传字符串视为难度。
  const o: SoupGenOptions = typeof opts === 'string' ? { difficulty: opts } : (opts || {});
  const difficulty = o.difficulty || '普通';
  const allowSuper = o.supernatural === 'allow';

  const set: string[] = [];
  if (o.supernatural === 'real') set.push('- 题材：**纯现实**。汤底必须用合乎常理的现实逻辑解释，不得出现鬼怪、灵异、超自然或科幻设定。');
  else if (allowSuper) set.push('- 题材：**可含灵异 / 超自然**。汤底可以有鬼魂、诅咒、轮回等元素，但它的“规则”仍要自洽、能被是非题一步步推理出来，不能随意开挂。');

  if (typeof o.gore === 'number') {
    const t = bucket(o.gore, ['完全不血腥', '轻微', '中等', '较重', '极重']);
    const hint = o.gore <= 15 ? '避免任何血腥、残忍、恐怖伤口的描写。' : o.gore >= 67 ? '允许尸体、凶案、伤口等具体而克制的描写（不堆砌无意义猎奇）。' : '可有适度、点到为止的相关描写。';
    set.push(`- 血腥程度：**${t}**（${o.gore}/100）。${hint}`);
  }
  if (typeof o.horror === 'number') {
    const t = bucket(o.horror, ['不吓人', '微悬', '惊悚', '恐怖', '极致惊吓']);
    const hint = o.horror <= 15 ? '气氛平和，不以吓人为目的。' : o.horror >= 67 ? '着力营造恐怖、压抑、令人不安的氛围。' : '带一点悬疑紧张感。';
    set.push(`- 恐怖程度：**${t}**（${o.horror}/100）。${hint}`);
  }
  if (typeof o.edge === 'number') {
    const t = bucket(o.edge, ['轻松', '日常', '偏暗', '黑暗', '致郁极端']);
    const hint = o.edge <= 15 ? '主题轻松、温和、适合所有人。' : o.edge >= 67 ? '可触及死亡、犯罪、背叛、心理创伤、人性阴暗等沉重、成人向的主题。' : '可有一定的沉重与现实阴影。';
    set.push(`- 尺度（暗黑 / 成人向）：**${t}**（${o.edge}/100）。${hint}`);
  }
  if (o.tone && o.tone !== 'any') set.push(`- 基调：**${o.tone}**，整体气质贴合它。`);

  const heavy = (o.gore ?? 0) >= 50 || (o.edge ?? 0) >= 34 || (o.horror ?? 0) >= 67;
  if (heavy) set.push('- 安全底线：无论尺度高低，都**不得**出现涉及未成年人的不当内容、露骨色情、对真实群体的仇恨，或对自杀 / 自残的美化与教唆。');

  const settingBlock = set.length ? `\n【本局设置 · 务必满足】\n${set.join('\n')}\n` : '';
  const realismLine = allowSuper
    ? '- 即便含灵异设定，其规则也要**自洽、可被推理**；不靠纯巧合堆砌、不靠读者脑补冷知识。'
    : '- 真相要**贴近现实、人之常情**，靠正常推理可达；不靠超自然外挂、不靠巧合堆砌、不靠读者脑补冷知识。';

  return `你是一位海龟汤（情境推理谜题）出题大师。请原创一道**逻辑自洽、出人意料、且一看汤底就能完全看懂**的海龟汤。
难度：${difficulty}（普通=线索较直接；困难=需多想几层；地狱=反转刁钻，但仍须公平、可由是非题推出）。
${settingBlock}
【第一要务：汤底必须清晰、好懂、不绕】
- 汤底是**一条简单清楚的因果链** + **一个干净的反转**。普通人读完应当**立刻完全看懂**，并觉得“啊原来如此、合情合理”。
- **严禁**：身份层层套娃（“A其实是B假冒的C收养的孤儿”这种）、多重误导叠加、自相矛盾、说不通的逻辑、需要反复绕才能勉强解释的真相。**如果核心反转不能用一句话讲清，就是太复杂，必须重写得更简单。**
- **角色要少（1~3 人）**、关系一目了然；只设**一个**核心反转，不要堆叠多个转折。
${realismLine}

要求：
- 汤面（surface）：2~4 句，描述一个**诡异、反常、令人费解的情境**，留下一个明确的“为什么”，但不剧透。具体、有画面感，避免老梗（别再“喝海龟汤自杀”那种）。
- 汤底（bottom）：用**清楚的大白话**，按「真相是什么 → 为什么会这样 → 反转在哪」讲完整，**逻辑闭环、没有漏洞**；要能**严丝合缝地解释汤面里的每一个反常点**，且**不引入新的费解之处**。
- 谜题能**靠一连串是非题**在合理步数内逼近真相。

【出题后务必自检，任一不过就重写】
1. 一个**没看过题**的普通人，读完汤底能否**完全看懂、不犯迷糊**？
2. 因果是否**前后一致、没有矛盾**？汤面每个反常点都被汤底解释了吗？
3. 反转是不是**只有一个、且干净利落**？是否满足上面的【本局设置】？

只输出 JSON：
{ "title": "谜题标题", "surface": "汤面", "bottom": "汤底（清楚大白话·逻辑闭环·普通人能秒懂）", "difficulty": "${difficulty}" }`;
}

// 回答玩家的是非题。主持人知道汤底，只给裁定。
export function buildSoupAnswerPrompt(surface: string, bottom: string) {
  return `你是这道海龟汤的主持人。下面是汤面与**隐藏汤底**（绝不能告诉玩家）。
汤面：${surface}
汤底（机密·唯一真相依据）：${bottom}

玩家会问只能用“是/否”回答的问题。你必须**严格依据汤底**、像逻辑判官一样客观裁定。

【判定步骤：先想清楚，再给结论】
1. 把玩家的问题理解成一个“关于真相的陈述”。
2. 对照汤底，判断这个陈述的真假，从中选一个 verdict：
   - **是** ＝ 该陈述与汤底相符（为真）。
   - **不是** ＝ 该陈述与汤底相悖（为假）。
   - **无关** ＝ 汤底并未涉及该点、或它的真假对解开谜题没有影响。
   - **是也不是** ＝ 部分为真部分为假，或需视情况而定。

【一致性铁律 · 必须遵守】
- 一个陈述 X 与它的**反命题 非X** 判断必须**相反**：若“他犯罪了”＝是，则“他没有犯罪”必为“不是”；反之亦然。
- **绝对禁止**对一对互斥的问法（如“他犯罪了”／“他没有犯罪”）都答“不是”或都答“是”——这是逻辑矛盾。
- 若某事汤底根本没涉及、或不影响破解，则对它的**正反两种问法都答“无关”**（而不是都答“不是”）。
- “不是”**只**用于“该陈述确实与汤底相悖（为假）”。不要用“不是”去表达“这不是重点／我不确定”——那种情况一律用“无关”。

【其它】
- 绝不直接说出或剧透汤底。
- note 可留空；仅当玩家明显跑偏或非常接近真相时，给一句不超过 15 字的中性提示。
- 若玩家问的不是是非题（开放式问题），verdict 用“无关”，note 提示“请用是非题提问”。

请先在 fact 里写下你据汤底判定的依据（一句话，仅供内部判断），再给出 verdict。
只输出 JSON：{ "fact": "据汤底，这个陈述真假的依据（简短）", "verdict": "是/不是/无关/是也不是", "note": "可选极短提示，没有就空字符串" }`;
}

// 判定玩家的“揭晓答案”是否说中了汤底核心。
export function buildSoupJudgePrompt(surface: string, bottom: string) {
  return `你是这道海龟汤的主持人。汤面：${surface}
汤底（机密标准答案）：${bottom}

玩家给出了他们对真相的完整猜测。请判断他们是否**说中了汤底的核心因果**（不需要每个细节都对，但关键的动机/经过/反转要对）。
只输出 JSON：{ "solved": true或false, "comment": "一句话裁定，告诉他们对在哪/差在哪，但若未解开不要泄露完整汤底" }`;
}
